// Render dynamic card grid representing all agents
function renderActiveGrid() {
    const grid = document.getElementById('agents-grid');
    grid.innerHTML = '';
    
    // Combined list: seedAgents + custom registeredAgents
    const allAgents = [...registeredAgents, ...seedAgents];
    
    allAgents.forEach(agent => {
        // Filter category if active
        if (activeCategoryFilter !== 'all' && agent.category !== activeCategoryFilter) {
            return;
        }
        
        // Resolve localized description
        let descText = '';
        if (agent.descEn) {
            // Seed agent
            const langKey = 'desc' + currentLang.charAt(0).toUpperCase() + currentLang.slice(1);
            descText = agent[langKey] || agent.descEn;
        } else {
            // Custom agent
            descText = agent.description;
        }
        
        const card = document.createElement('div');
        card.className = 'card';
        card.setAttribute('data-category', agent.category);
        
        const statusClass = agent.status === 'online' ? 'status-online' : 'status-away';
        const localizedStatus = agent.status === 'online' ? translations[currentLang].statusOnline : translations[currentLang].statusAway;
        
        // Tags rendering
        let tagsHtml = '';
        const tagsToRender = agent.tags || ["Autonomous", "API", "Protocol-402"];
        tagsToRender.forEach(tag => {
            tagsHtml += `<span>${tag}</span>`;
        });
        
        card.innerHTML = `
            <div class="card-header">
                <div class="agent-meta">
                    <h3>${agent.name}</h3>
                    <span class="${statusClass}">${localizedStatus}</span>
                </div>
                <span class="price-tag">$${parseFloat(agent.price).toFixed(3)} / call</span>
            </div>
            <p class="description">${descText}</p>
            <div class="tags">
                ${tagsHtml}
            </div>
            <button class="btn-card" onclick="openProtocol402Query('${agent.name}', ${agent.price}, '${agent.id}')">${translations[currentLang].queryBtn}</button>
        `;
        grid.appendChild(card);
    });
}

// Dynamic Registry Fee Calculator
function calculateDynamicFee(category) {
    if (category === 'security' || category === 'oracle') return 5.00; // Enterprise Audit
    if (['defi', 'research', 'datasets'].includes(category)) return 2.50; // Premium
    return 1.00; // Standard
}

