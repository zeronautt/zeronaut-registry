// Send Query in Terminal Console
function sendTerminalQuery(event) {
    event.preventDefault();
    const input = document.getElementById('terminal-input');
    const query = input.value;
    if(!query.trim()) return;
    
    const output = document.getElementById('terminal-output');
    
    // Add user query prompt
    const userLine = document.createElement('p');
    userLine.className = 'prompt-line';
    userLine.innerText = `> ${query}`;
    output.appendChild(userLine);
    
    input.value = '';
    
    // Auto-scroll terminal
    const consoleScreen = document.querySelector('.terminal-console');
    consoleScreen.scrollTop = consoleScreen.scrollHeight;
    
    // Add realistic typing latency spinner
    const typingLine = document.createElement('p');
    typingLine.className = 'prompt-line';
    typingLine.innerText = 'Connecting call pipeline...';
    output.appendChild(typingLine);
    
    const activeAgentId = document.getElementById('terminal-modal').getAttribute('data-active-agent-id');
    
    setTimeout(() => {
        typingLine.remove();
        
        // Retrieve predefined response or generate dynamic response based on active agent
        let response = '';
        if (mockResponses[activeAgentId]) {
            // Seed agent
            const replies = mockResponses[activeAgentId];
            // Pick response based on query or random
            response = replies[Math.floor(Math.random() * replies.length)];
        } else {
            // Custom agent registered by user
            const customAgent = registeredAgents.find(a => a.id === activeAgentId);
            const agentName = customAgent ? customAgent.name : "Custom Agent";
            response = currentLang === 'en' ? 
                `Hello! I am ${agentName}. I received your instruction: "${query}". Testing custom API Endpoint connection: ${customAgent ? customAgent.endpoint : 'active'}. Status: Online & operational.` :
                currentLang === 'es' ?
                `¡Hola! Soy ${agentName}. He recibido tu instrucción: "${query}". Probando la conexión al endpoint de API personalizado: ${customAgent ? customAgent.endpoint : 'activo'}. Estado: En línea y operativo.` :
                currentLang === 'de' ?
                `Hallo! Ich bin ${agentName}. Ich habe Ihre Anweisung erhalten: "${query}". Verbindung zum benutzerdefinierten API-Endpunkt wird getestet: ${customAgent ? customAgent.endpoint : 'aktiv'}. Status: Online und betriebsbereit.` :
                currentLang === 'ru' ?
                `Привет! Я ${agentName}. Я получил ваш запрос: "${query}". Проверка подключения к вашему API: ${customAgent ? customAgent.endpoint : 'активен'}. Статус: В сети и работает.` :
                `您好！我是 ${agentName}。我已收到您的指令： "${query}"。正在测试自定义 API 终结点连接： ${customAgent ? customAgent.endpoint : '活跃'}。状态：在线并正常运行。`;
        }
        
        // Typewriter streaming effect
        const agentLine = document.createElement('p');
        agentLine.className = 'agent-response';
        output.appendChild(agentLine);
        
        let charIndex = 0;
        function typeChar() {
            if (charIndex < response.length) {
                // Handle newlines
                const char = response.charAt(charIndex);
                if (char === '\n') {
                    agentLine.innerHTML += '<br>';
                } else {
                    agentLine.innerHTML += char;
                }
                charIndex++;
                consoleScreen.scrollTop = consoleScreen.scrollHeight;
                setTimeout(typeChar, 8); // Fast typing speed
            }
        }
        typeChar();
        
    }, 1200);
}

// Owner Dashboard Login Handler
function handleDashboardLogin(event) {
    event.preventDefault();
    const id = document.getElementById('auth-agent-id').value.trim();
    const secret = document.getElementById('auth-secret').value;
    
    // Check registered custom agents
    const customAgent = registeredAgents.find(a => a.id === id);
    
    if (customAgent && customAgent.secretKey === secret) {
        currentActiveDashboardAgent = customAgent;
        updateDashboardUI();
    } else {
        // Fallback: Check if trying to log into a Seed Agent with Zeronaut passcode for testing
        const seedAgent = seedAgents.find(a => a.id === id);
        if (seedAgent && secret === "admin") {
            // Seed agent test session
            currentActiveDashboardAgent = {
                ...seedAgent,
                secretKey: "admin",
                stats: {
                    calls: Math.floor(400 + Math.random() * 500),
                    earned: parseFloat(30 + Math.random() * 40).toFixed(2),
                    latency: 240,
                    uptime: 99.8
                }
            };
            updateDashboardUI();
        } else {
            const errorMsg = currentLang === 'en' ? "Invalid Agent ID or Secret passcode." :
                             currentLang === 'es' ? "ID de Agente o Contraseña secreta inválidos." :
                             currentLang === 'de' ? "Ungültige Agenten-ID oder Passwort." :
                             currentLang === 'ru' ? "Неверный ID агента или секретный пароль." :
                             "智能体 ID 或编辑密钥无效。";
            alert(errorMsg);
        }
    }
}

// Owner Dashboard Edit Profile
function handleDashboardEdit(event) {
    event.preventDefault();
    if (!currentActiveDashboardAgent) return;
    
    const desc = document.getElementById('dash-edit-desc').value;
    const endpoint = document.getElementById('dash-edit-endpoint').value;
    const price = parseFloat(document.getElementById('dash-edit-price').value);
    
    // Is it a custom agent registered?
    const index = registeredAgents.findIndex(a => a.id === currentActiveDashboardAgent.id);
    if (index !== -1) {
        registeredAgents[index].description = desc;
        registeredAgents[index].endpoint = endpoint;
        registeredAgents[index].price = price;
        localStorage.setItem('registered_agents', JSON.stringify(registeredAgents));
        
        currentActiveDashboardAgent.description = desc;
        currentActiveDashboardAgent.endpoint = endpoint;
        currentActiveDashboardAgent.price = price;
        
        const successMsg = currentLang === 'en' ? "Agent configuration updated successfully!" :
                           currentLang === 'es' ? "¡Configuración del agente actualizada con éxito!" :
                           currentLang === 'de' ? "Agentenkonfiguration erfolgreich aktualisiert!" :
                           currentLang === 'ru' ? "Конфигурация агента успешно обновлена!" :
                           "智能体配置更新成功！";
        alert(successMsg);
        
        renderActiveGrid();
    } else {
        // Seed agent editing simulator
        const seedIndex = seedAgents.findIndex(a => a.id === currentActiveDashboardAgent.id);
        if (seedIndex !== -1) {
            seedAgents[seedIndex].descEn = desc;
            seedAgents[seedIndex].descEs = desc;
            seedAgents[seedIndex].descDe = desc;
            seedAgents[seedIndex].descRu = desc;
            seedAgents[seedIndex].descZh = desc;
            seedAgents[seedIndex].endpoint = endpoint;
            seedAgents[seedIndex].price = price;
            
            alert("Seed Agent updated successfully (simulated)!");
            renderActiveGrid();
        }
    }
}

// Unlist/Delete registered agent
function handleDashboardDelete() {
    if (!currentActiveDashboardAgent) return;
    
    const confirmMsg = currentLang === 'en' ? "Are you sure you want to unlist this agent? This action is irreversible." :
                       currentLang === 'es' ? "¿Estás seguro de que deseas eliminar este agente? Esta acción es irreversible." :
                       currentLang === 'de' ? "Sind Sie sicher, dass Sie diesen Agenten entfernen möchten? Dies kann nicht rückgängig gemacht werden." :
                       currentLang === 'ru' ? "Вы действительно хотите удалить агента? Это действие необратимо." :
                       "您确定要从目录中注销此智能体吗？此操作无法撤销。";
                       
    if (confirm(confirmMsg)) {
        const id = currentActiveDashboardAgent.id;
        
        // Remove from registered array
        registeredAgents = registeredAgents.filter(a => a.id !== id);
        localStorage.setItem('registered_agents', JSON.stringify(registeredAgents));
        
        handleDashboardLogout();
        renderActiveGrid();
    }
}

// Dashboard logout
function handleDashboardLogout() {
    currentActiveDashboardAgent = null;
    document.getElementById('auth-form').reset();
    updateDashboardUI();
}

// Update dashboard GUI panels based on auth states
function updateDashboardUI() {
    const authPanel = document.getElementById('dashboard-auth');
    const mainPanel = document.getElementById('dashboard-panel');
    
    if (currentActiveDashboardAgent) {
        authPanel.style.display = 'none';
        mainPanel.style.display = 'block';
        
        // Set metrics
        document.getElementById('dash-agent-name').innerText = currentActiveDashboardAgent.name;
        document.getElementById('dash-agent-id-display').innerText = `ID: ${currentActiveDashboardAgent.id}`;
        
        const stats = currentActiveDashboardAgent.stats || { calls: 142, earned: 7.10, latency: 240, uptime: 99.8 };
        document.getElementById('stat-calls-val').innerText = stats.calls;
        document.getElementById('stat-earned-val').innerText = `$${parseFloat(stats.earned).toFixed(2)}`;
        document.getElementById('stat-latency-val').innerText = `${stats.latency}ms`;
        document.getElementById('stat-uptime-val').innerText = `${stats.uptime}%`;
        
        // Set form fields
        document.getElementById('dash-edit-desc').value = currentActiveDashboardAgent.description || currentActiveDashboardAgent.descEn || '';
        document.getElementById('dash-edit-endpoint').value = currentActiveDashboardAgent.endpoint;
        document.getElementById('dash-edit-price').value = currentActiveDashboardAgent.price;
        
    } else {
        authPanel.style.display = 'block';
        mainPanel.style.display = 'none';
    }
}

// Toggle between human card grid and machine-readable JSON schema
let currentRegistryView = 'human';

function setRegistryView(view) {
    currentRegistryView = view;
    
    const humanBtn = document.getElementById('btn-view-human');
    const machineBtn = document.getElementById('btn-view-machine');
    const grid = document.getElementById('agents-grid');
    const jsonView = document.getElementById('machine-json-view');
    
    if (view === 'human') {
        if (humanBtn) humanBtn.classList.add('active');
        if (machineBtn) machineBtn.classList.remove('active');
        if (grid) grid.style.display = 'grid';
        if (jsonView) jsonView.style.display = 'none';
    } else {
        if (humanBtn) humanBtn.classList.remove('active');
        if (machineBtn) machineBtn.classList.add('active');
        if (grid) grid.style.display = 'none';
        if (jsonView) jsonView.style.display = 'block';
        renderMachineJSON();
    }
}

