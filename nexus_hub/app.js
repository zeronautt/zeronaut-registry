// Dispatch events to local cloud monitoring daemon
function dispatchNotification(eventData) {
    fetch('http://localhost:3000/api/event', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify(eventData)
    }).catch(err => {
        console.log('[Cloud Monitor] Monitor agent is offline. Skipping API notification.');
    });
}

